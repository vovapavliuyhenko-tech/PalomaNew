const bySlug: Record<string, BlogPost> = Object.fromEntries(
  BLOG_POSTS.map((p) => [p.slug, p])
);

export function getBlogPostBySlug(slug: string): BlogPost | undefined {
  return bySlug[slug];
}

export function getAllBlogSlugs(): string[] {
  return BLOG_POSTS.map((p) => p.slug);
}

export type BlogCategoryFilter = { key: string; label: string };

/** Уникальные рубрики в порядке появления в ленте */
export function getBlogCategoryFilters(): BlogCategoryFilter[] {
  const seen = new Set<string>();
  const out: BlogCategoryFilter[] = [];
  for (const p of BLOG_POSTS) {
    if (seen.has(p.categoryKey)) continue;
    seen.add(p.categoryKey);
    out.push({ key: p.categoryKey, label: p.categoryLabel });
  }
  return out;
}
