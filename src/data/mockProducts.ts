export const getProductsByCategory = (category: string) =>
  mockProducts.filter((p) => p.category === category && p.inStock);

export const getBestsellers = () =>
  mockProducts.filter((p) => p.isBestseller && p.inStock);

export const getSeasonalProducts = () =>
  mockProducts.filter((p) => p.isSeasonal && p.inStock);

/** Букеты и подарки для витрины (без напитков). */
export const getFlowerShopProducts = () =>
  mockProducts.filter((p) => p.inStock && p.category !== "coffee");

export const getMinPrice = (p: MockProduct) =>
  Math.min(...p.sizes.map((s) => s.price));

/** Допродажи в корзине: открытка, ваза, кофе. */
const CART_DRAWER_ADDON_IDS = ["gift-01", "vase-01", "coffee-01", "coffee-02"] as const;

export const getCartDrawerAddonProducts = (): MockProduct[] =>
  CART_DRAWER_ADDON_IDS.map((id) => mockProducts.find((p) => p.id === id)).filter(
    (p): p is MockProduct => Boolean(p?.inStock)
  );

export const getProductBySlug = (slug: string) =>
  mockProducts.find((p) => p.slug === slug);

export const getRelatedProducts = (productId: string) => {
  const product = mockProducts.find((p) => p.id === productId);
  if (!product) return [];
  return mockProducts.filter(
    (p) => product.relatedProductIds.includes(p.id) && p.inStock
  );
};

export const getUpsellProducts = (productId: string) => {
  const product = mockProducts.find((p) => p.id === productId);
  if (!product) return [];
  return mockProducts.filter(
    (p) => product.upsellProductIds.includes(p.id) && p.inStock
  );
};
